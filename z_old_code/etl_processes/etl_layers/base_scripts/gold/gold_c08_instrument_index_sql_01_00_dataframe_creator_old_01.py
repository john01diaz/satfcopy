def __rename_columns(
        dataframe: pandas.DataFrame,
        column_rename_dict: Dict[str, str]) \
        -> pandas.DataFrame:
    return \
        dataframe.rename(
            columns=column_rename_dict)


def __get_distinct_rows(
        dataframe: pandas.DataFrame) \
        -> pandas.DataFrame:
    return \
        dataframe.drop_duplicates()
